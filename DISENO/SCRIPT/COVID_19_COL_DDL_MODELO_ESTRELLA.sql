CREATE TABLE dbo.TBL_DIM_CASO_T2 (
    SK_DIM_CASO INT IDENTITY(1,1) PRIMARY KEY,
    INT_ID_CASO INT NULL,
    STR_TIPO_CONTAGIO NVARCHAR(50) NOT NULL,
    STR_UBICACION_CASO NVARCHAR(50) NOT NULL,
    STR_ESTADO NVARCHAR(50) NOT NULL,
    STR_CONDICION_FINAL NVARCHAR(50) NOT NULL,
    STR_TIPO_RECUPERACION NVARCHAR(50) NULL,
	DT_FECHA_REPORTE_WEB DATE NULL,
    DT_FECHA_CARGUE DATETIME NULL,
    DT_FECHA_INICIO DATETIME NULL,
    DT_FECHA_VIGENCIA DATETIME NULL,
    INT_FLAG TINYINT NULL
);

-- TEXTO COMANDO SQL - ORIGEN DE OLE DB

SELECT FECHA_REPORTE_WEB,
	ID_CASO,
	TIPO_CONTAGIO,
	UBICACION_CASO,
	ESTADO,
	CONDICION_FINAL,
	TIPO_RECUPERACION
FROM [DWH_COVID_19_COLOMBIA].[dbo].[TBL_CASOS_POSITIVOS];

-- USAR RESULTADO DE CONSULTA SQL - LOOKUP

SELECT 
    t.SK_DIM_CASO,
    TRY_CAST(
        CONCAT(
            FORMAT(t.DT_FECHA_REPORTE_WEB, 'yyyyMMdd'),
            CAST(CAST(t.INT_ID_CASO AS BIGINT) AS VARCHAR(20))
        ) AS BIGINT
    ) AS LLAVE_INSERT_D,
    CONCAT(
        COALESCE(LTRIM(RTRIM(t.STR_TIPO_CONTAGIO)), ''),
        COALESCE(LTRIM(RTRIM(t.STR_UBICACION_CASO)), ''),
        COALESCE(LTRIM(RTRIM(t.STR_ESTADO)), ''),
        COALESCE(LTRIM(RTRIM(t.STR_CONDICION_FINAL)), ''),
        COALESCE(LTRIM(RTRIM(t.STR_TIPO_RECUPERACION)), '')
    ) AS LLAVE_UPDATE_D,
    t.INT_FLAG AS FLAG_D
FROM dbo.TBL_DIM_CASO_T2 t
WHERE t.INT_FLAG = 1  -- Solo registros vigentes

