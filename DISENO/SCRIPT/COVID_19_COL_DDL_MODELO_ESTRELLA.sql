CREATE TABLE dbo.TBL_FACT_CASOS (
	SK_DIM_CASO INT NULL,
	SK_DIM_PERSONA INT NULL,
	SK_DIM_UBICACION INT NULL,
	SK_DIM_TIEMPO INT NULL,
	BIT_RECUPERADO BIT NULL,
	BIT_MUERTO BIT NULL,
	INT_DIAS_REPORTE INT NULL,
	INT_DIAS_RETRASO_DIAGNOSTICO INT NULL,
	INT_DIAS_RECUPERACION INT NULL,
	INT_DIAS_HASTA_MUERTE INT NULL,
	DT_FECHA_CARGUE DATETIME NULL
);

-- COMANDO SQL - EJECUTAR TAREA SQL

TRUNCATE TABLE dbo.TBL_FACT_CASOS;

-- TEXTO COMANDO SQL - ORIGEN DE OLE DB

SELECT 
	TRY_CAST(FORMAT(FECHA_NOTIFICACION, 'yyyyMMdd') AS BIGINT) AS SK_DIM_TIEMPO,
	CONCAT(NOMBRE_DEPARTAMENTO, NOMBRE_MUNICIPIO) AS LLAVE_UBICACION,
	EDAD,
	CASE
        WHEN EDAD BETWEEN 1 AND 5 THEN 'PRIMERA INFANCIA (1–5)'
        WHEN EDAD BETWEEN 6 AND 11 THEN 'INFANCIA INTERMEDIA (6–11)'
        WHEN EDAD BETWEEN 12 AND 14 THEN 'ADOLESCENCIA TEMPRANA (12–14)'
        WHEN EDAD BETWEEN 15 AND 17 THEN 'ADOLESCENCIA MEDIA (15–17)'
        WHEN EDAD BETWEEN 18 AND 20 THEN 'ADOLESCENCIA TARDÍA (18–20)'
        WHEN EDAD BETWEEN 21 AND 24 THEN 'ADULTEZ EMERGENTE (21–24)'
        WHEN EDAD BETWEEN 25 AND 39 THEN 'ADULTEZ JOVEN (25–39)'
        WHEN EDAD BETWEEN 40 AND 54 THEN 'ADULTEZ MEDIA (40–54)'
        WHEN EDAD BETWEEN 55 AND 64 THEN 'ADULTEZ MEDIA TARDÍA (55–64)'
        WHEN EDAD BETWEEN 65 AND 74 THEN 'ADULTEZ MAYOR TEMPRANA (65–74)'
        WHEN EDAD BETWEEN 75 AND 84 THEN 'ADULTEZ MAYOR (75–84)'
        WHEN EDAD BETWEEN 85 AND 94 THEN 'VEJEZ AVANZADA (85–94)'
        WHEN EDAD BETWEEN 95 AND 113 THEN 'LONGEVIDAD EXTREMA (95–113)'
        ELSE 'FUERA DE RANGO'
    END AS CATEGORIA_EDAD,
	SEXO,
	ID_CASO AS LLAVE_CASO,
	CASE 
		WHEN UPPER(LTRIM(RTRIM(CONDICION_FINAL))) = 'RECUPERADO' THEN 1
		ELSE 0
	END AS BIT_RECUPERADO,
	CASE 
		WHEN UPPER(LTRIM(RTRIM(CONDICION_FINAL))) = 'FALLECIDO' OR UPPER(LTRIM(RTRIM(CONDICION_FINAL))) = 'N/A' THEN 1
		ELSE 0
	END AS BIT_MUERTO,
	DATEDIFF(DAY, FECHA_DIAGNOSTICO, FECHA_REPORTE_WEB) AS INT_DIAS_REPORTE,
	DATEDIFF(DAY, FECHA_INICIO_SINTOMAS, FECHA_DIAGNOSTICO) AS INT_DIAS_RETRASO_DIAGNOSTICO,
	DATEDIFF(DAY, FECHA_INICIO_SINTOMAS, FECHA_RECUPERACION) AS INT_DIAS_RECUPERACION,
    DATEDIFF(DAY, FECHA_INICIO_SINTOMAS, FECHA_MUERTE) AS INT_DIAS_HASTA_MUERTE
FROM dbo.TBL_CASOS_POSITIVOS;

-- USAR RESULTADO DE CONSULTA SQL - LOOKUP TBL_DIM_UBICACION_T0

SELECT SK_DIM_UBICACION, CONCAT(STR_DEPARTAMENTO, STR_MUNICIPIO) LLAVE_UBICACION_D
FROM dbo.TBL_DIM_UBICACION_T0;

-- USAR RESULTADO DE CONSULTA SQL - LOOKUP TBL_DIM_PERSONA_T0

SELECT SK_DIM_PERSONA, CONCAT(INT_EDAD, STR_CATEGORIA_EDAD, CHR_SEXO) LLAVE_PERSONA_D
FROM dbo.TBL_DIM_PERSONA_T0;

